bool Tools::isSpecialChar(char c) {
	return c == '*' || c == '^' || c == '#' || c == '@' || c == '>' || c == '<' || c == ')' || c == ';'
		|| c == '!' || c == '&' || c == '%' || c == '(' || c == ')' || c == '~' || c == '$' || c == '?'
		|| c == '{' || c == '}' || c == '[' || c == ']' || c == ':' || c == '|' || c == '+' || c == '-';
}